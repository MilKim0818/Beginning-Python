# 변수
# : 메모리 공간에 부여한 이름
# : 이름을 통해서 메모리 접근
# : 변하는 값 (좁은 의미)

num = 5
print('num >>', num)
print('type >>', type(num))

num = 2.5
print('num >>', num)
print('type >>', type(num))

num = 'hi'
print('num >>', num)
print('type >>', type(num))

