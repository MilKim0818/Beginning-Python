## [문제 1]
## 함수 정의 
def four_rules(n1, n2):
    print(f'{n1} + {n2} = {n1+n2}')
    print(f'{n1} - {n2} = {n1-n2}')
    print(f'{n1} * {n2} = {n1*n2}')
    print(f'{n1} / {n2} = {n1//n2}')

## 함수 호출
four_rules(7, 2)

